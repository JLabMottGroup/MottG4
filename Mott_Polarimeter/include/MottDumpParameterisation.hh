#pragma once

#ifndef MottDumpParameterisation_H
#define MottDumpParameterisation_H

#include "globals.hh"
#include "G4VPVParameterisation.hh"

class G4VPhysicalVolume;
class G4Box;

// Dummy declarations to get rid of warnings ...
class G4Trd;
class G4Trap;
class G4Cons;
class G4Orb;
class G4Sphere;
class G4Torus;
class G4Para;
class G4Hype;
class G4Tubs;
class G4Polycone;
class G4Polyhedra;

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

class MottDumpParameterisation : public G4VPVParameterisation
{ 
  public:
  
    MottDumpParameterisation(G4int NoLayers, 
                             G4double startZ, 
                             G4double spacingZ,
                             G4double depthChamber,
                             G4int NoRings,
                             G4double radialStep);

    virtual                                 
   ~MottDumpParameterisation();
   
    void ComputeTransformation (const G4int copyNo,
                                G4VPhysicalVolume* physVol) const;
    
    void ComputeDimensions (G4Tubs& solidVol, const G4int copyNo, const G4VPhysicalVolume* physVol) const;


  private:  // Dummy declarations to get rid of warnings ...

    void ComputeDimensions (G4Box&,const G4int,const G4VPhysicalVolume*) const {}
    void ComputeDimensions (G4Trd&,const G4int,const G4VPhysicalVolume*) const {}
    void ComputeDimensions (G4Trap&,const G4int,const G4VPhysicalVolume*) const {}
    void ComputeDimensions (G4Cons&,const G4int,const G4VPhysicalVolume*) const {}
    void ComputeDimensions (G4Sphere&,const G4int,const G4VPhysicalVolume*) const {}
    void ComputeDimensions (G4Orb&,const G4int,const G4VPhysicalVolume*) const {}
    void ComputeDimensions (G4Torus&,const G4int,const G4VPhysicalVolume*) const {}
    void ComputeDimensions (G4Para&,const G4int,const G4VPhysicalVolume*) const {}
    void ComputeDimensions (G4Hype&,const G4int,const G4VPhysicalVolume*) const {}
    void ComputeDimensions (G4Polycone&,const G4int,const G4VPhysicalVolume*) const {}
    void ComputeDimensions (G4Polyhedra&,const G4int,const G4VPhysicalVolume*) const {}

  private:

    G4int    fNoLayers;		// # Of layers
    G4int    fNoRings;		// # Of rings
    G4double fStartZ;		// Z position of center of first layer
    G4double fHalfDepth;        // Half-depth of each ring layer
    G4double fSpacing;          // Space between centers of adjacent layers
    G4double fRadialStep;	// Radius change between each concentric ring
    
};

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

#endif


